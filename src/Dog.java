public class Dog {
    public static void makeVoice() {
        int weight = 10;
        if (weight < 10) {
            System.out.println("awaw");
        }
        else if (weight < 30) {
            System.out.println("bark");
        }
        else if (weight < 60) {
            System.out.println("woof");
        }
    }
}
